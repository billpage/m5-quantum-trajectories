"""
m5_sim.py — Unified simulation driver for M5 quantum trajectories.
=================================================================

Provides a single entry point for running 1-D M5 simulations in two modes:

  'grid'      — ψ-KDE via CIC deposit + Gaussian smoothing on a spatial grid.
                Fields (v, Q) derived from ψ̂ by finite differences.
                Candidates generated by Gaussian random noise (Nelson diffusion).
                Backward channel: u′ = ν ∂²(ln ρ)/∂x² computed on grid.
                GPU via xp (numpy/cupy) abstraction.

  'gridless'  — Swarmalator with shared GH candidate cloud.
                Fields computed via direct kernel sums at particle positions.
                STEER selects candidate ∝ √ρ via GH quadrature.
                WEIGH computes Q via M₊ ratio.
                Backward channel: u′ via GH-weighted ln ρ (Holland bi-HJ).
                GPU via xp (numpy/cupy) abstraction.

Both modes share:
  • Ensemble input via m5_init.Ensemble dataclass
  • Phase update: dS = (½mv² − V − Q)·dt with phase wrapping
  • Common return dict: X, S, t_save, wall_time, mode, params
  • Optional trajectory tracking for selected particle IDs
  • Backward channel: Q̃ = Q + ℏu′ (diagnostic; Q used in dynamics)

Usage
-----
    from m5_init import init_ensemble_1d, Ensemble
    from m5_sim import m5_simulate

    x = np.linspace(-15, 15, 512, endpoint=False)
    psi0 = my_wavefunction(x)
    X, S = init_ensemble_1d(psi0, x, Np=4000, psi0_func=my_wavefunction)
    ens = Ensemble(X=X, S=S, x_grid=x)

    result = m5_simulate(ens, V_func=my_V, T=2.0, Nt=2000,
                         mode='gridless', K_gh=6, sigma_gh=0.20, h_kde=0.22)
"""

import numpy as np
import time

from m5_init import Ensemble


# ═══════════════════════════════════════════════════════════════════════
# Backend selection: cupy (GPU) or numpy (CPU)
# ═══════════════════════════════════════════════════════════════════════

def select_backend(force=None):
    """Return (xp, backend_name).

    Parameters
    ----------
    force : 'cpu' | 'gpu' | None
        None auto-detects (prefers GPU if cupy available).
    """
    if force == 'cpu':
        return np, 'numpy (CPU)'
    try:
        import cupy as cp
        _ = cp.array([1.0])
        if force == 'gpu' or force is None:
            name = cp.cuda.runtime.getDeviceProperties(0)["name"].decode()
            return cp, f'cupy (GPU: {name})'
    except Exception as e:
        if force == 'gpu':
            raise RuntimeError(f"GPU requested but cupy unavailable: {e}")
    return np, 'numpy (CPU)'


# ═══════════════════════════════════════════════════════════════════════
# Shared helpers (xp-compatible: numpy or cupy)
# ═══════════════════════════════════════════════════════════════════════

PI = 3.141592653589793


def _Dx(f, dx, xp):
    """Central-difference first derivative (periodic BC)."""
    return (xp.roll(f, -1) - xp.roll(f, 1)) / (2.0 * dx)


def _D2x(f, dx, xp):
    """Central-difference second derivative (periodic BC)."""
    return (xp.roll(f, -1) - 2.0 * f + xp.roll(f, 1)) / (dx * dx)


def _gaussian_smooth(f, sigma, xp):
    """FFT-based Gaussian smoothing (periodic BC, works with numpy/cupy).

    Parameters
    ----------
    f     : (N,) real or complex array
    sigma : float — bandwidth in grid cells
    xp    : numpy or cupy module
    """
    N = len(f)
    is_complex = xp.iscomplexobj(f)
    if is_complex:
        # Smooth real and imaginary parts separately
        return (_gaussian_smooth(xp.real(f), sigma, xp) +
                1j * _gaussian_smooth(xp.imag(f), sigma, xp))
    k = xp.fft.rfftfreq(N)               # 0, 1/N, 2/N, ...
    gauss_k = xp.exp(-2.0 * (PI * sigma * k)**2)
    return xp.fft.irfft(gauss_k * xp.fft.rfft(f), n=N)


def _linear_interp(X, x_grid, field, xp):
    """Interpolate 1-D field to particle positions (xp-compatible).

    Parameters
    ----------
    X       : (Np,) particle positions
    x_grid  : (Nx,) uniform grid
    field   : (Nx,) values on grid
    xp      : numpy or cupy

    Returns
    -------
    (Np,) interpolated values
    """
    xL = float(x_grid[0])
    dx = float(x_grid[1] - x_grid[0])
    Nx = len(x_grid)
    fi = xp.clip((X - xL) / dx, 0.0, Nx - 1.001)
    lo = fi.astype(xp.int64)
    al = fi - lo
    hi = xp.minimum(lo + 1, Nx - 1)
    return (1.0 - al) * field[lo] + al * field[hi]


# ═══════════════════════════════════════════════════════════════════════
# Grid-mode field estimation (CIC deposit + Gaussian smooth)
# ═══════════════════════════════════════════════════════════════════════

def _grid_psi_kde(X, S, x_grid, sigma, hbar, xp):
    """
    CIC deposit + FFT Gaussian convolution → ψ̂ = j_h / √n_h.

    Uses xp.bincount for GPU-compatible CIC deposit.

    Parameters
    ----------
    X     : (Np,) particle positions (on device)
    S     : (Np,) action phases (on device)
    x_grid: (Nx,) spatial grid (on device)
    sigma : float — Gaussian bandwidth in grid cells
    hbar  : float
    xp    : numpy or cupy

    Returns
    -------
    psi_grid : (Nx,) complex — normalised ψ̂ on grid
    sqrt_rho : (Nx,) float  — |ψ̂|
    """
    Np = len(X)
    Nx = len(x_grid)
    xL = float(x_grid[0])
    dx = float(x_grid[1] - x_grid[0])
    eps_n = 1e-30

    # Fractional grid index
    fi = xp.clip((X - xL) / dx, 0.0, Nx - 1.001)
    k_L = fi.astype(xp.int64)
    alpha = fi - k_L
    k_R = xp.minimum(k_L + 1, Nx - 1)

    phi = S / hbar
    cos_phi = xp.cos(phi)
    sin_phi = xp.sin(phi)

    w_L = 1.0 - alpha
    w_R = alpha

    # CIC deposit via bincount (GPU-compatible)
    n_raw = (xp.bincount(k_L, weights=w_L, minlength=Nx) +
             xp.bincount(k_R, weights=w_R, minlength=Nx))
    j_re = (xp.bincount(k_L, weights=w_L * cos_phi, minlength=Nx) +
            xp.bincount(k_R, weights=w_R * cos_phi, minlength=Nx))
    j_im = (xp.bincount(k_L, weights=w_L * sin_phi, minlength=Nx) +
            xp.bincount(k_R, weights=w_R * sin_phi, minlength=Nx))

    # Gaussian convolution (single bandwidth)
    n_sm = _gaussian_smooth(n_raw, sigma, xp) / Np
    j_re_sm = _gaussian_smooth(j_re, sigma, xp) / Np
    j_im_sm = _gaussian_smooth(j_im, sigma, xp) / Np

    # ψ̂ = j / √n, then normalise
    sqrt_n = xp.sqrt(xp.maximum(n_sm, eps_n))
    psi_re = j_re_sm / sqrt_n
    psi_im = j_im_sm / sqrt_n

    # Normalise: ∫|ψ̂|² dx = 1
    rho_raw = psi_re**2 + psi_im**2
    norm = xp.sqrt(xp.sum(rho_raw) * dx)
    if float(norm) > eps_n:
        psi_re = psi_re / norm
        psi_im = psi_im / norm

    sqrt_rho = xp.sqrt(psi_re**2 + psi_im**2)
    psi_grid = psi_re + 1j * psi_im
    return psi_grid, sqrt_rho


# ═══════════════════════════════════════════════════════════════════════
# Gridless-mode kernel sums (direct ψ-KDE at arbitrary points)
# ═══════════════════════════════════════════════════════════════════════

def kernel_sums(eval_x, X_src, phi_src, h, xp, cutoff=4.0,
                need_deriv=False, chunk_size=2048):
    """
    Compute ψ-KDE kernel sums at evaluation points.

    eval_x:   (N_eval,)  — points where fields are needed
    X_src:    (N_src,)    — source particle positions
    phi_src:  (N_src,)    — source particle phases S/ℏ
    h:        scalar      — kernel bandwidth
    xp:       module      — numpy or cupy

    Returns dict with:
      n:       (N_eval,)  — density KDE  Σ K_h(x-X_j)
      j_re:    (N_eval,)  — Re Σ K_h(x-X_j) cos(φ_j)
      j_im:    (N_eval,)  — Im Σ K_h(x-X_j) sin(φ_j)
    If need_deriv:
      jp_re:   (N_eval,)  — Re Σ K′_h(x-X_j) cos(φ_j)
      jp_im:   (N_eval,)  — Im Σ K′_h(x-X_j) sin(φ_j)
    """
    N_eval = len(eval_x)
    cos_phi = xp.cos(phi_src)
    sin_phi = xp.sin(phi_src)

    n_out = xp.zeros(N_eval, dtype=xp.float64)
    j_re_out = xp.zeros(N_eval, dtype=xp.float64)
    j_im_out = xp.zeros(N_eval, dtype=xp.float64)
    if need_deriv:
        jp_re_out = xp.zeros(N_eval, dtype=xp.float64)
        jp_im_out = xp.zeros(N_eval, dtype=xp.float64)

    inv_h = 1.0 / h
    norm = inv_h / (2.0 * PI) ** 0.5
    radius = cutoff * h

    for i0 in range(0, N_eval, chunk_size):
        i1 = min(i0 + chunk_size, N_eval)
        dx = eval_x[i0:i1, None] - X_src[None, :]

        mask = xp.abs(dx) < radius
        u = dx * inv_h
        K = xp.where(mask, norm * xp.exp(-0.5 * u * u), 0.0)

        n_out[i0:i1] = K.sum(axis=1)
        j_re_out[i0:i1] = (K * cos_phi[None, :]).sum(axis=1)
        j_im_out[i0:i1] = (K * sin_phi[None, :]).sum(axis=1)

        if need_deriv:
            Kp = xp.where(mask, -(dx / (h * h)) * K, 0.0)
            jp_re_out[i0:i1] = (Kp * cos_phi[None, :]).sum(axis=1)
            jp_im_out[i0:i1] = (Kp * sin_phi[None, :]).sum(axis=1)

    result = dict(n=n_out, j_re=j_re_out, j_im=j_im_out)
    if need_deriv:
        result['jp_re'] = jp_re_out
        result['jp_im'] = jp_im_out
    return result


def psi_kde_fields(ks, xp, hbar=1.0, mass=1.0, eps_n=1e-30):
    """
    From raw kernel sums, compute √ρ, ln ρ, and optionally v, u.

    ks: dict from kernel_sums()
    Returns dict with sqrt_rho, ln_rho, and if derivatives present: v, u
    """
    nu = hbar / (2.0 * mass)
    n = xp.maximum(ks['n'], eps_n)
    j_abs = xp.sqrt(ks['j_re']**2 + ks['j_im']**2)
    sqrt_rho = j_abs / xp.sqrt(n)
    ln_j = xp.log(xp.maximum(j_abs, eps_n))
    ln_n = xp.log(n)
    ln_rho = 2.0 * ln_j - ln_n

    result = dict(sqrt_rho=sqrt_rho, ln_rho=ln_rho, n=n,
                  j_re=ks['j_re'], j_im=ks['j_im'], j_abs=j_abs)

    if 'jp_re' in ks:
        j2 = ks['j_re']**2 + ks['j_im']**2
        eps_coh = 1e-4
        coherent = j2 > eps_coh * n * n
        j2_safe = xp.where(coherent, j2, 1.0)

        im_ratio = (ks['jp_im'] * ks['j_re'] -
                     ks['jp_re'] * ks['j_im']) / j2_safe
        re_ratio = (ks['jp_re'] * ks['j_re'] +
                     ks['jp_im'] * ks['j_im']) / j2_safe

        result['v'] = xp.where(coherent, (hbar / mass) * im_ratio, 0.0)
        result['u'] = xp.where(coherent, nu * re_ratio, 0.0)

    return result


# ═══════════════════════════════════════════════════════════════════════
# GH quadrature setup
# ═══════════════════════════════════════════════════════════════════════

def gh_nodes_weights(K_gh):
    """Gauss–Hermite nodes and normalised weights (CPU).

    Uses physicist's Hermite polynomials (weight exp(−t²), E[ξ²]=½).
    Combined with probe offsets η = √2·σ_gh·ξ, the probe variance is:
        E[η²] = 2·σ_gh²·E[ξ²] = 2·σ_gh²·½ = σ_gh²
    """
    from numpy.polynomial.hermite import hermgauss
    xi, omega = hermgauss(K_gh)
    omega /= omega.sum()
    return xi, omega


# ═══════════════════════════════════════════════════════════════════════
# Public API
# ═══════════════════════════════════════════════════════════════════════

def m5_simulate(ensemble, V_func, T, Nt, *,
                mode='gridless',
                save_every=50, seed=42,
                track_ids=None,
                verbose=True,
                # ── Grid-mode parameters ─────────────────────────
                sigma_kde=2.5,        # KDE bandwidth (grid cells)
                K_cand=48,            # candidates per particle
                sigma_Q_smooth=1.5,   # extra smoothing on Q field
                # ── Gridless-mode parameters ─────────────────────
                K_gh=6,               # GH quadrature order
                sigma_gh=0.20,        # GH probe scale (physical)
                h_kde=0.25,           # kernel bandwidth (physical)
                # ── Backend ──────────────────────────────────────
                backend=None,         # 'cpu' | 'gpu' | None (auto)
                chunk_size=2048):     # max rows in kernel matrices
    """
    Run a 1-D M5 quantum trajectory simulation.

    Parameters
    ----------
    ensemble : Ensemble
        Particle state (X, S) and domain info (x_grid, hbar, mass).
        Created via m5_init.init_ensemble_1d + m5_init.Ensemble.
    V_func : callable
        V(x) → real array, the external potential.
    T : float
        Total propagation time.
    Nt : int
        Number of time steps.
    mode : {'grid', 'gridless'}
        'grid'     — CIC deposit + Gaussian smooth on spatial grid.
        'gridless' — GH-quadrature swarmalator with Holland bi-HJ.
    save_every : int
        Save a snapshot every this many steps.
    seed : int
        Random seed for stochastic selection during dynamics.
    track_ids : array-like of int, or None
        If provided, record positions of these particle indices at every
        time step (not just snapshots).  Useful for trajectory plots.
    verbose : bool
        Print progress to stdout.

    Grid-mode parameters
    --------------------
    sigma_kde : float
        Gaussian smoothing bandwidth in grid-cell units for CIC deposit.
    K_cand : int
        Number of random-noise candidates per particle per step.
    sigma_Q_smooth : float
        Light Gaussian smooth (grid cells) applied to Q field.

    Gridless-mode parameters
    ------------------------
    K_gh : int
        Gauss–Hermite quadrature order (shared STEER + WEIGH cloud).
    sigma_gh : float
        GH probe scale in physical units.
    h_kde : float
        Kernel bandwidth in physical units.

    Backend
    -------
    backend : 'cpu' | 'gpu' | None
        None auto-detects (prefers GPU if cupy is available).
    chunk_size : int
        Maximum rows in vectorised kernel-sum matrices (memory control).

    Returns
    -------
    dict with keys:
        X         : (Ns, Np) float — particle positions at snapshots
        S         : (Ns, Np) float — particle phases at snapshots
        t_save    : (Ns,) float    — snapshot times
        wall_time : float          — total wall-clock time (seconds)
        mode      : str            — 'grid' or 'gridless'
        params    : dict           — copy of simulation parameters

    Grid-mode extras:
        psi       : (Ns, Nx) complex — ψ̂ on grid at snapshots
        v_field   : (Ns, Nx) float   — velocity field at snapshots
        Q_field   : (Ns, Nx) float   — quantum potential at snapshots
        Q_tilde   : (Ns, Nx) float   — Q̃ = Q + ℏu′ (backward channel)

    Gridless-mode extras:
        h_kde     : float            — kernel bandwidth used

    If track_ids is not None:
        traj_X    : (Nt+1, N_track) float — tracked positions at every step
        traj_t    : (Nt+1,) float         — time array for trajectories
    """
    if mode not in ('grid', 'gridless'):
        raise ValueError(f"Unknown mode '{mode}'; use 'grid' or 'gridless'.")

    # ── Unpack ensemble ───────────────────────────────────────────────
    hbar = ensemble.hbar
    mass = ensemble.mass
    x_grid = ensemble.x_grid
    Nx = ensemble.Nx
    dx = ensemble.dx
    xL = ensemble.xL
    xR = ensemble.xR
    Np = ensemble.Np
    dt = T / Nt

    # ── Backend selection ─────────────────────────────────────────────
    xp, backend_name = select_backend(backend)
    if verbose:
        print(f"  Backend: {backend_name}", flush=True)

    # ── Precompute V on grid and transfer to device ───────────────────
    V_grid_cpu = V_func(np.asarray(x_grid))
    V_grid_dev = xp.asarray(V_grid_cpu, dtype=xp.float64)
    x_grid_dev = xp.asarray(x_grid, dtype=xp.float64)

    # ── Particle state on CPU ─────────────────────────────────────────
    X_cpu = np.array(ensemble.X, dtype=np.float64)
    S_cpu = np.array(ensemble.S, dtype=np.float64)

    # ── Parameter snapshot (for reproducibility) ──────────────────────
    params = dict(
        xL=xL, xR=xR, Nx=Nx, T=T, Nt=Nt, Np=Np, dt=dt, dx=dx,
        mode=mode, hbar=hbar, mass=mass, backend=backend_name,
        save_every=save_every, seed=seed)

    # ── Dispatch ──────────────────────────────────────────────────────
    if mode == 'grid':
        params.update(sigma_kde=sigma_kde, K_cand=K_cand,
                      sigma_Q_smooth=sigma_Q_smooth)
        return _grid_sim(
            X_cpu, S_cpu, x_grid_dev, dx, Nx, V_grid_dev, dt, Nt,
            hbar=hbar, mass=mass, Np=Np, xL=xL, xR=xR,
            sigma_kde=sigma_kde, K_cand=K_cand,
            sigma_Q_smooth=sigma_Q_smooth,
            save_every=save_every, seed=seed,
            track_ids=track_ids, verbose=verbose,
            xp=xp, params=params)

    else:  # gridless
        params.update(K_gh=K_gh, sigma_gh=sigma_gh, h_kde=h_kde,
                      chunk_size=chunk_size)
        return _gridless_sim(
            X_cpu, S_cpu, x_grid_dev, dx, Nx, V_grid_dev, dt, Nt,
            hbar=hbar, mass=mass, Np=Np, xL=xL, xR=xR,
            K_gh=K_gh, sigma_gh=sigma_gh, h_kde=h_kde,
            save_every=save_every, seed=seed,
            track_ids=track_ids, verbose=verbose,
            xp=xp, chunk_size=chunk_size, params=params)


# ═══════════════════════════════════════════════════════════════════════
# Grid-mode simulation
# ═══════════════════════════════════════════════════════════════════════

def _grid_sim(X_cpu, S_cpu, x_grid_dev, dx, Nx, V_grid_dev, dt, Nt, *,
              hbar, mass, Np, xL, xR,
              sigma_kde, K_cand, sigma_Q_smooth,
              save_every, seed, track_ids, verbose, xp, params):
    """
    Grid-based ψ-KDE simulation.

    Fields are reconstructed on a spatial grid via CIC deposit and
    FFT-based Gaussian smoothing, then interpolated to particle positions.
    Includes backward channel: u′ = ν ∂²(ln ρ)/∂x², Q̃ = Q + ℏu′.
    """
    nu = hbar / (2.0 * mass)
    sigma_noise = (hbar / mass) ** 0.5
    eps = 1e-30

    rng = np.random.default_rng(seed)

    # Transfer to device
    X = xp.asarray(X_cpu, dtype=xp.float64)
    S = xp.asarray(S_cpu, dtype=xp.float64)

    def to_cpu(arr):
        return arr.get() if hasattr(arr, 'get') else np.array(arr)

    # ── Snapshot storage ──────────────────────────────────────────────
    idx_save = set(range(0, Nt + 1, save_every))
    Ns = len(idx_save)
    X_hist = np.zeros((Ns, Np))
    S_hist = np.zeros((Ns, Np))
    psi_hist = np.zeros((Ns, Nx), dtype=np.complex128)
    v_hist = np.zeros((Ns, Nx))
    Q_hist = np.zeros((Ns, Nx))
    Qt_hist = np.zeros((Ns, Nx))
    t_hist = np.zeros(Ns)
    si = 0

    # ── Trajectory tracking ───────────────────────────────────────────
    if track_ids is not None:
        track_ids_arr = np.asarray(track_ids, dtype=int)
        traj_X = np.zeros((Nt + 1, len(track_ids_arr)))
        traj_X[0] = X_cpu[track_ids_arr]
    else:
        track_ids_arr = None
        traj_X = None

    # ── Initial snapshot ──────────────────────────────────────────────
    if 0 in idx_save:
        psi0_est, _ = _grid_psi_kde(X, S, x_grid_dev, sigma_kde, hbar, xp)
        X_hist[si] = X_cpu.copy()
        S_hist[si] = S_cpu.copy()
        psi_hist[si] = to_cpu(psi0_est)
        # v, Q, Q̃ all zero at t=0
        t_hist[si] = 0.0
        si += 1

    t0 = time.time()

    for n in range(1, Nt + 1):

        # ── STEP 1: ψ-KDE field estimation ───────────────────────────
        psi_grid, sqrt_rho = _grid_psi_kde(
            X, S, x_grid_dev, sigma_kde, hbar, xp)
        rho_est = sqrt_rho ** 2

        # Current velocity: v = (ℏ/m) Im(ψ̂* ∂ψ̂) / |ψ̂|²
        dpsi = _Dx(psi_grid, dx, xp)
        v_field = ((hbar / mass)
                   * xp.imag(xp.conj(psi_grid) * dpsi)
                   / xp.maximum(rho_est, eps))

        # Quantum potential: Q = -(ℏ²/2m) ∂²√ρ / √ρ
        Q_field = (-(hbar**2 / (2.0 * mass))
                   * _D2x(sqrt_rho, dx, xp)
                   / xp.maximum(sqrt_rho, eps))
        Q_field = _gaussian_smooth(Q_field, sigma_Q_smooth, xp)

        # Backward channel: u′ = ν ∂²(ln ρ)/∂x², Q̃ = Q + ℏu′
        ln_rho = xp.log(xp.maximum(rho_est, eps))
        u_prime_field = nu * _D2x(ln_rho, dx, xp)
        Q_tilde_field = Q_field + hbar * u_prime_field

        # ── STEP 2: Classical advection ───────────────────────────────
        v_at = _linear_interp(X, x_grid_dev, v_field, xp)
        X_class = X + v_at * dt

        # ── STEP 3: √ρ-weighted candidate selection ──────────────────
        noise_cpu = rng.normal(size=(Np, K_cand)) * sigma_noise * (dt**0.5)
        noise = xp.asarray(noise_cpu, dtype=xp.float64)
        cands = xp.clip(X_class[:, None] + noise, xL + dx, xR - dx)

        # Interpolate √ρ at candidates
        fi = xp.clip((cands - xL) / dx, 0.0, Nx - 1.001)
        lo = xp.clip(fi.astype(xp.int64), 0, Nx - 2)
        hi = lo + 1
        al = xp.clip(fi - lo, 0.0, 1.0)
        w = xp.maximum((1.0 - al) * sqrt_rho[lo] + al * sqrt_rho[hi], eps)

        # Categorical selection
        w_norm = w / w.sum(axis=1, keepdims=True)
        cum = xp.cumsum(w_norm, axis=1)
        u_rand = xp.asarray(rng.uniform(size=Np), dtype=xp.float64)
        ck = xp.clip(
            (cum < u_rand[:, None]).sum(axis=1).astype(xp.int64),
            0, K_cand - 1)
        X = cands[xp.arange(Np), ck]

        # ── STEP 4: Phase update ──────────────────────────────────────
        Q_at = _linear_interp(X, x_grid_dev, Q_field, xp)
        V_at = _linear_interp(X, x_grid_dev, V_grid_dev, xp)
        S = S + (0.5 * mass * v_at**2 - V_at - Q_at) * dt

        TWO_PI_HBAR = 2.0 * PI * hbar
        S = xp.mod(S + 0.5 * TWO_PI_HBAR, TWO_PI_HBAR) - 0.5 * TWO_PI_HBAR

        # ── Trajectory tracking ───────────────────────────────────────
        if traj_X is not None:
            traj_X[n] = to_cpu(X)[track_ids_arr]

        # ── Snapshot ──────────────────────────────────────────────────
        if n in idx_save and si < Ns:
            X_hist[si] = to_cpu(X)
            S_hist[si] = to_cpu(S)
            psi_hist[si] = to_cpu(psi_grid)
            v_hist[si] = to_cpu(v_field)
            Q_hist[si] = to_cpu(Q_field)
            Qt_hist[si] = to_cpu(Q_tilde_field)
            t_hist[si] = n * dt
            si += 1

        if verbose and n % max(1, Nt // 10) == 0:
            print(f"    step {n}/{Nt}  ({time.time() - t0:.1f}s)", flush=True)

    # Fill remaining slots
    for j in range(si, Ns):
        X_hist[j] = to_cpu(X)
        S_hist[j] = to_cpu(S)
        psi_hist[j] = to_cpu(psi_grid)
        v_hist[j] = to_cpu(v_field)
        Q_hist[j] = to_cpu(Q_field)
        Qt_hist[j] = to_cpu(Q_tilde_field)
        t_hist[j] = Nt * dt

    wall_time = time.time() - t0
    result = dict(
        X=X_hist, S=S_hist, t_save=t_hist,
        wall_time=wall_time, mode='grid', params=params,
        psi=psi_hist, v_field=v_hist,
        Q_field=Q_hist, Q_tilde=Qt_hist)

    if traj_X is not None:
        result['traj_X'] = traj_X
        result['traj_t'] = np.linspace(0, Nt * dt, Nt + 1)

    return result


# ═══════════════════════════════════════════════════════════════════════
# Gridless (swarmalator) simulation
# ═══════════════════════════════════════════════════════════════════════

def _gridless_sim(X_cpu, S_cpu, x_grid_dev, dx, Nx, V_grid_dev, dt, Nt, *,
                  hbar, mass, Np, xL, xR,
                  K_gh, sigma_gh, h_kde,
                  save_every, seed, track_ids, verbose,
                  xp, chunk_size, params):
    """
    Gridless swarmalator simulation with Holland bi-HJ.

    All kernel sums are vectorised as batched matrix operations.
    Supports GPU via the xp (numpy/cupy) abstraction.
    """
    nu = hbar / (2.0 * mass)
    h = h_kde
    rng = np.random.default_rng(seed)

    # ── GH nodes (CPU then transfer) ─────────────────────────────────
    xi_cpu, omega_cpu = gh_nodes_weights(K_gh)
    xi = xp.asarray(xi_cpu, dtype=xp.float64)
    omega = xp.asarray(omega_cpu, dtype=xp.float64)

    # ── Transfer to device ────────────────────────────────────────────
    X = xp.asarray(X_cpu, dtype=xp.float64)
    S = xp.asarray(S_cpu, dtype=xp.float64)

    def to_cpu(arr):
        return arr.get() if hasattr(arr, 'get') else np.array(arr)

    # ── Snapshot storage ──────────────────────────────────────────────
    idx_save = set(range(0, Nt + 1, save_every))
    Ns = len(idx_save)
    X_hist = []; S_hist = []; t_hist = []

    # ── Trajectory tracking ───────────────────────────────────────────
    if track_ids is not None:
        track_ids_arr = np.asarray(track_ids, dtype=int)
        traj_X = np.zeros((Nt + 1, len(track_ids_arr)))
        traj_X[0] = X_cpu[track_ids_arr]
    else:
        track_ids_arr = None
        traj_X = None

    if 0 in idx_save:
        X_hist.append(to_cpu(X.copy()))
        S_hist.append(to_cpu(S.copy()))
        t_hist.append(0.0)

    t0 = time.time()

    for n in range(1, Nt + 1):
        phi = S / hbar

        # ══════════════ STEP 1: VELOCITY ══════════════════════════════
        ks_vel = kernel_sums(X, X, phi, h, xp,
                             need_deriv=True, chunk_size=chunk_size)
        fields_vel = psi_kde_fields(ks_vel, xp, hbar=hbar, mass=mass)
        v_at = fields_vel['v']

        # ══════════════ STEP 2: CLASSICAL ADVECTION ═══════════════════
        X_class = X + v_at * dt
        X_class = xp.clip(X_class, xL + h, xR - h)

        # ══════════════ STEP 3: SHARED GH CANDIDATE CLOUD ════════════
        cand_offsets = (2.0 ** 0.5) * sigma_gh * xi       # (K_gh,)
        cands = X_class[:, None] + cand_offsets[None, :]   # (Np, K_gh)
        cands = xp.clip(cands, xL + h, xR - h)
        cands_flat = cands.reshape(-1)

        all_eval = xp.concatenate([cands_flat, X_class])

        # ══════════════ STEP 4: ψ-KDE AT ALL POINTS ══════════════════
        ks_all = kernel_sums(all_eval, X, phi, h, xp,
                             need_deriv=False, chunk_size=chunk_size)
        f_all = psi_kde_fields(ks_all, xp, hbar=hbar, mass=mass)

        sqr_cands = f_all['sqrt_rho'][:Np * K_gh].reshape(Np, K_gh)
        lnr_cands = f_all['ln_rho'][:Np * K_gh].reshape(Np, K_gh)
        sqr_depart = f_all['sqrt_rho'][Np * K_gh:]
        lnr_depart = f_all['ln_rho'][Np * K_gh:]

        # ══════════════ STEP 5: STEER (selection) ═════════════════════
        w_sel = omega[None, :] * xp.maximum(sqr_cands, 1e-30)
        w_sum = w_sel.sum(axis=1, keepdims=True)
        probs = w_sel / xp.maximum(w_sum, 1e-30)

        cum = xp.cumsum(probs, axis=1)
        u_rand = xp.asarray(rng.uniform(size=Np), dtype=xp.float64)
        chosen = xp.clip(
            (cum < u_rand[:, None]).sum(axis=1).astype(xp.int64),
            0, K_gh - 1)

        idx_row = xp.arange(Np)
        X = cands[idx_row, chosen]

        # ══════════════ STEP 6: FORWARD WEIGH (Q) ════════════════════
        M_plus = (omega[None, :] * sqr_cands).sum(axis=1)
        ratio = M_plus / xp.maximum(sqr_depart, 1e-30)
        Q_at = -(hbar**2 / (mass * sigma_gh**2)) * (ratio - 1.0)

        Q_med = xp.median(xp.abs(Q_at))
        Q_at = xp.clip(Q_at, -50.0 * Q_med - 1.0, 50.0 * Q_med + 1.0)

        # ══════════════ STEP 7: BACKWARD CHANNEL ═════════════════════
        L_k = (omega[None, :] * lnr_cands).sum(axis=1)
        u_prime = nu * 2.0 * (L_k - lnr_depart) / (sigma_gh**2)
        Q_tilde = Q_at + hbar * u_prime

        # ══════════════ STEP 8: PHASE UPDATE ══════════════════════════
        fi = xp.clip((X - xL) / dx, 0.0, Nx - 1.001)
        lo = fi.astype(xp.int64)
        al = fi - lo
        hi = xp.minimum(lo + 1, Nx - 1)
        V_at = (1.0 - al) * V_grid_dev[lo] + al * V_grid_dev[hi]

        S = S + (0.5 * mass * v_at**2 - V_at - Q_at) * dt

        TWO_PI_HBAR = 2.0 * PI * hbar
        S = xp.mod(S + 0.5 * TWO_PI_HBAR, TWO_PI_HBAR) - 0.5 * TWO_PI_HBAR

        # ── Trajectory tracking ───────────────────────────────────────
        if traj_X is not None:
            traj_X[n] = to_cpu(X)[track_ids_arr]

        # ── Snapshot ──────────────────────────────────────────────────
        if n in idx_save:
            X_hist.append(to_cpu(X.copy()))
            S_hist.append(to_cpu(S.copy()))
            t_hist.append(n * dt)

        if verbose and n % max(1, Nt // 10) == 0:
            print(f"    step {n}/{Nt}  ({time.time() - t0:.1f}s)", flush=True)

    wall_time = time.time() - t0
    result = dict(
        X=np.array(X_hist), S=np.array(S_hist),
        t_save=np.array(t_hist),
        wall_time=wall_time, mode='gridless', params=params,
        h_kde=h)

    if traj_X is not None:
        result['traj_X'] = traj_X
        result['traj_t'] = np.linspace(0, Nt * dt, Nt + 1)

    return result
